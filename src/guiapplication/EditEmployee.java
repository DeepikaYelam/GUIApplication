    public void populateArrayList(){
        try{
            FileInputStream file=new FileInputStream("Jobs.dat");
            ObjectInputStream input= new ObjectInputStream(file);
            boolean endOfFile=false;
            while(!endOfFile){
                try{
                    jobs.add((Job)input.readObject());
                } catch(EOFException e){
                    endOfFile=true;
                } catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            input.close();
            
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        try{
            FileInputStream file2=new FileInputStream("Employees.dat");
            ObjectInputStream input2=new ObjectInputStream(file2);
            boolean endOfFile=false;
            while(!endOfFile){
                try{
                    employees.add((Employee) input2.readObject());
                } catch(EOFException e){
                    endOfFile=true;
                } catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            input2.close();
        } catch (IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void saveEmployeesToFile(){
        try{
            FileOutputStream inputFile=new FileOutputStream("Employees.dat");
            ObjectOutputStream output=new ObjectOutputStream(inputFile);
            for(int i=0; i<employees.size(); i++){
                output.writeObject(employees.get(i));
            }
            output.close();
            JOptionPane.showMessageDialog(null, "Successfully saved");
            this.dispose();
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
