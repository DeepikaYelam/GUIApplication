    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()|| jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
        }
        else {
            int selectedIndex=jComboBox1.getSelectedIndex();
            employees.get(selectedIndex).setFirstName(jTextField2.getText().trim());
            employees.get(selectedIndex).setLastName(jTextField3.getText().trim());
            employees.get(selectedIndex).setEmpNum(Integer.parseInt(jTextField1.getText().trim()));
            Job job=jobs.get(jComboBox2.getSelectedIndex());
            employees.get(selectedIndex).setJob(job);
            saveEmployeesToFile();
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
         int selectedIndex=jComboBox1.getSelectedIndex();
        jTextField2.setText(employees.get(selectedIndex).getFirstName());
        jTextField3.setText(employees.get(selectedIndex).getLastName());
        jTextField1.setText(employees.get(selectedIndex).getEmpNum()+"");
        int index=0;
        Job job=employees.get(selectedIndex).getJob();
        for(int i=0;i<jobs.size();i++){
            if(jobs.get(i).equals(job)){
              index=i;
              break;  
            }
            
        }
        jComboBox2.setSelectedIndex(index);
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

